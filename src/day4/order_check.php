$order_amount = 5200;

if ($order_amount >= 5000) {
    $shipping = 0; // 送料無料
} else {
    $shipping = 500; // 送料は500円
}

$total = $order_amount + $shipping;

echo "合計金額は{$total} 円です。<br>";
