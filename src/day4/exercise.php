<?php
// 【練習問題】
// ✅ 配列 $numbers の中から「10以上かつ100未満」の数字だけを表示してください。
// ✅ 表示形式は「数字: ○○」という形で、<br> で改行して出力してください。

// 📝 条件まとめ
// ✅ $numbers 配列の中から
// ✅ 10以上かつ100未満の数字だけを
// ✅ 「数字: ○○」という形で1行ずつ
// ✅ ブラウザに <br> で改行して出力してください。


// （ヒント）foreach と if を使います。

// 以下の配列を使います：
$numbers = [12, 5, 8, 130, 44, 3, 100, 25];


// 配列の要素を1つずつ取り出す
foreach ($numbers as $number) {
    // もし数字が 10以上 かつ 100未満 なら
    if ($number >= 10 && $number < 100) {
        // 条件に合う数字だけを表示
        echo "数字: $number<br>";
    }
}


// 🎯 Day4 追加の練習問題 ⓵

// 📝 【問題】偶数の合計を求めて表示する
// 以下の配列があります。

// $numbers = [3, 7, 2, 8, 10, 1, 9, 4];

// この配列の中から、偶数の数字だけを合計し、
// 最後に
// 偶数の合計: ○○
// という形式で、1回だけ表示してください。

// ✅ 出力は <br> をつけてブラウザで表示してください。

// 📝 条件まとめ
// 配列の中から偶数だけを見つける
// 偶数を全部足す
// 合計値を echo で出力
// 表示形式：「偶数の合計: ○○」
// 出力は <br> で改行

// 💡 （小さなヒント）
// 数字が偶数かどうかは → if ($number % 2 == 0) で判断できます。
// 合計を計算するために、最初に $sum = 0; と用意しておくと良いです。

// ーーーーーーーーーーーーーーーーーーーーーーー

// 最初からコードを書こうとすると
// 頭がフリーズしてしまうので日本語で処理の流れを書いてみる。



// 🎯 日本語での処理の流れ（ステップ）
// 1️⃣ 数字を入れた配列を用意する
// → 配列 $numbers に数字を入れる。

// 2️⃣ 合計値を入れるための変数を用意する
// → $sum という変数に 0 を入れて「最初の合計」を作る。

// 3️⃣ 配列の数字を1つずつ取り出すために繰り返し処理を使う
// → foreach を使って配列の中の数字を順番に $number に取り出す。

// 4️⃣ 取り出した数字が偶数かどうかを調べる
// → if 文で「2で割った余りが0かどうか」を確認する。

// 5️⃣ もし偶数だったら合計にその数字を足す
// → $sum に $number を足す。

// 6️⃣ 全部の数字を調べ終わったら、合計を画面に表示する
// → echo で「偶数の合計: ○○」と出力する。


// 📝 なぜこの順序なの？
// 💬 配列の数字を1つずつ見て → 偶数かを判断して → その都度合計に足していく
// という**「順次処理 + 条件分岐 + 繰り返し」**が必要だからです。

// 👉 先に合計用の変数を用意しないと「どこに足すか」がなくて困ります。
// 👉 ループの中で毎回 echo しないのは「最後に1回だけ合計を出したいから」です。

// ✅ この日本語ステップがそのままコメントに変わる！
// ↓


// 配列を定義
// $numbers = [...];
$numbers = [3, 7, 2, 8, 10, 1, 9, 4];

// 合計値を入れる変数を用意
// $sum = 0;
$sum = 0;

// 配列の要素を1つずつ取り出す
// foreach (...) {
foreach($numbers as $number) {

// もし数字が偶数だったら
// if (...) {
    if ($number % 2 == 0) {

// 合計にその数字を足す
    $sum = $sum + $number;
    }
}

// 合計を表示する
echo "偶数の合計: $sum<br>";
// という流れに置き換えられます。
