<?php

// 📘 Day4教材：loop_control.php
// 🎯 テーマ：ループの制御（break / continue）

// 🧠 1. break文の基本
// 🔰 解説：
// break文は、ループの途中で「処理を強制的に終了」させる構文です。
// たとえば、条件に達した時点でループをやめたい場合に使います。

echo "【break文の例】<br>";

for ($i = 1; $i <= 10; $i++) {
    if ($i === 5) {
        break; // 5になったらループを終了
    }
    echo "カウント: $i<br>";
}

echo "※ 5になった時点でループを終了しました。<br><br>";


// 🧠 2. continue文の基本
// 🔰 解説：
// continue文は、現在のループの「その1回の処理だけをスキップ」して、次の繰り返しに進みます。
// 条件に合ったときだけ処理を飛ばしたいときに使います。

echo "【continue文の例】<br>";

for ($i = 1; $i <= 7; $i++) {
    if ($i === 3 || $i === 6) {
        continue; // ３と６のときだけスキップ
    }
    echo "カウント: $i<br>";
}

echo "※ ３と６のときだけ出力をスキップしました。<br><br>";


// ✅ 解説のまとめ
// 文法	      効果	     よくある用途

// break	ループを完全に抜ける	一定条件で中断する（例：見つけたら終了）

// continue	その1回だけ処理をスキップ	条件に合うものだけ処理しない（例：3の倍数を除く など）