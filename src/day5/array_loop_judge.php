<?php
// 🎓 Day5 教材（array_loop_judge.php）
//
// 📝【概要】
// 配列に含まれる複数の数値データ（例：テストの点数や価格）に対して、
// 条件をもとに1件ずつ判定・表示していく処理を学びます。
// foreach文とif文の組み合わせに慣れるのが目的です。

echo "【配列内のデータを1件ずつ判定】<br><br>";

// ✅ 配列の用意（例：点数データ）
$scores = [85, 42, 77, 90, 59, 100, 61];

// ✅ 配列をforeach文でループ処理
foreach ($scores as $score) {

    // ✅ 条件分岐で判定ロジックを記述
    if ($score >= 80) {
        echo "点数: {$score} → 評価: 優秀<br>";
    } elseif ($score >= 60) {
        echo "点数: {$score} → 評価: 合格<br>";
    } else {
        echo "点数: {$score} → 評価: 不合格<br>";
    }
}


// 🔍 【補足】
// - $scores 配列に複数の点数データを格納
// - foreach文で各点数を1件ずつ $score に取り出す
// - if文で条件をチェックし、それぞれの評価を表示
//
// 🧠 応用すると：商品価格、年齢、名前のチェックなどにも応用可能です。
