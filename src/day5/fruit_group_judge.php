<?php
// 🎓 Day5 教材（fruit_group_judge.php）
//
// 🧠 テーマ：好きな果物ごとのグループに対して、if文で処理を分ける方法
//
// ✅ 特定の果物グループごとに異なるメッセージを表示
// ✅ 「みかん」は人気No.1、「りんご」は定番、「バナナ」は栄養満点、など
// ✅ 登場しない果物も想定し、「その他」も表示
//
// 🔰 ループとif文の組み合わせで処理を学ぶ教材です

echo "【果物ごとのメッセージ】<br><br>";

// 配列に果物のリストを用意
$fruits = ["りんご", "バナナ", "みかん", "ぶどう", "みかん", "バナナ"];

foreach ($fruits as $fruit) {
    // 果物ごとにメッセージを変える
    if ($fruit === "みかん") {
        echo "「{$fruit}」は人気No.1の果物です！<br>";
    } elseif ($fruit === "りんご") {
        echo "「{$fruit}」は定番ですね！<br>";
    } elseif ($fruit === "バナナ") {
        echo "「{$fruit}」は栄養満点です！<br>";
    } else {
        echo "「{$fruit}」もおいしいですね！<br>";
    }
}


// 📝 解説ポイント
// 項目	内容
// $fruits	複数の果物が入った配列。重複もOK。
// foreach	配列から果物を1つずつ取り出すループ構文。
// if / elseif / else	条件ごとに処理を分岐。該当がなければ「その他」。
// <br>	ブラウザでの改行用タグ。1行ずつ見やすく表示します。
