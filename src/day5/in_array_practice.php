// ✅ その下に『自分でPHPコード』を書いてください。
// （ここに自分でPHPコードを書いてください）
$fruits = ["りんご","メロン","オレンジ"];
$target = "ぶどう";

if (in_array($target,$fruits)) {
    echo "{$target}は含まれています<br>";
} else {
    echo "{$target}は含まれていません<br>";
}