// 連想配列を事前に作成
$fruit_counts = [];

// ユーザーごとに処理
foreach ($users as $user) {
    $fruit = $user["fruit"];

// 💡 `isset()` でその果物がすでに登録されているかを確認
// 💡 初めてなら「1」、すでにあるなら「+1」
    if (isset($fruit_counts [$fruit])) {
        $fruit_counts[$fruit]++;
    } else {
        $fruit_counts[$fruit] = 1;
    }
}
    // 💡 最後に foreach で集計結果を表示
    foreach ($fruit_counts as $fruit => $count) {
        echo "{$fruit}が好きな人は{$count}人です。<br>";
    }
