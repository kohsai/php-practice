<?php
// 📘 basic_syntax_summary.php
// 🧠 基本文法の図解まとめ：for文 / foreach文 / if文 / 括弧の使い方

echo "【PHPの基本構文まとめ】<br><br>";


// ------------------------------------
// 🔁 for文の基本構文
// ------------------------------------
// 例：1〜5までの数を順番に表示する

// 🔰 構文：
// for (初期化; 条件; 増減) {
//     繰り返したい処理;
// }

echo "▶ for文の例（1〜5まで）<br>";
for ($i = 1; $i <= 5; $i++) {
    echo "数字: $i <br>";
}
echo "<br>";


// ------------------------------------
// 🔁 foreach文の基本構文（配列用）
// ------------------------------------
// 例：フルーツのリストを1つずつ表示

// 🔰 構文：
// foreach ($配列 as $要素) {
//     繰り返したい処理;
// }

echo "▶ foreach文の例（配列）<br>";
$fruits = ["りんご", "バナナ", "みかん"];

foreach ($fruits as $fruit) {
    echo "フルーツ: $fruit <br>";
}
echo "<br>";


// ------------------------------------
// 🔀 if文の基本構文
// ------------------------------------
// 例：ある数値が10以上かどうかを判定

// 🔰 構文：
// if (条件) {
//     条件がtrueのときの処理;
// } elseif (別の条件) {
//     別の条件がtrueのときの処理;
// } else {
//     すべての条件がfalseのときの処理;
// }

echo "▶ if文の例（条件分岐）<br>";
$score = 75;

if ($score >= 80) {
    echo "評価：A <br>";
} elseif ($score >= 70) {
    echo "評価：B <br>";
} elseif ($score >= 60) {
    echo "評価：C <br>";
} else {
    echo "評価：D <br>";
}
echo "<br>";


// ------------------------------------
// 🧱 カッコの使い分け
// ------------------------------------
// 🔸 ( )：条件や引数の囲い → if文や関数などに使用
// 🔸 { }：処理のブロック囲い → for, if, 関数 などの中身
// 🔸 [ ]：配列の定義やアクセス
// 🔸 " "：文字列の囲い（変数展開あり）
// 🔸 ' '：文字列の囲い（変数展開なし）

echo "▶ カッコの使い方（例）<br>";

$colors = ["赤", "青", "緑"]; // [ ]：配列定義

foreach ($colors as $color) { // ( )：foreachの条件、{ }：処理ブロック
    echo "色は \"$color\" です。<br>"; // \" \"：文字列内の " をエスケープ
}
echo "<br>";


