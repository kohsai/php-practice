<?php
// 🎯 Day5 教材：ネストされたループと制御文の組み合わせ（nested_loop_control.php）

// 🧠 今回の目的：
// ネストされたループ（for文の中にfor文）で、continue や break を活用する方法を学びます。

// 📝 処理内容：
// 1〜9の段で九九表を作成し、以下の条件に従って出力します。

// ✅【条件】
// ・かける数（$j）が「1」のときはスキップ（continue）
// ・掛け算の結果が「30」を超えたら「終了」と表示して全体のループを終了（break）
// ・表示形式：「〇 × △ = 結果」
// ・各段の最後に <br> を入れて見やすく表示

echo "【条件付きの九九表】<br><br>";

for ($i = 1; $i <= 9; $i++) {
    for ($j = 1; $j <= 9; $j++) {

        // 💡 $jが1ならスキップ
        if ($j === 1) {
            continue;
        }

        $result = $i * $j;

        // 💡 掛け算の結果が30を超えたら終了
        if ($result > 30) {
            echo "→ 結果が30を超えたため終了します<br><br>";
            break 2;  // 2重ループをまとめて終了
        }

        echo "{$i} × {$j} = {$result}<br>";
    }

    echo "<br>";  // 段の区切り
}


// 💡 解説まとめ：
// continue は「その1回だけスキップ」する。

// break は「そのループ自体を終了」する。

// break 2 のように数字をつけると「外側のループ」まで一気に抜ける。

// ネストされたループ（forの中にfor）では、段の単位で出力整理すると見やすい。
