// 📝 【問題】
// 以下のユーザー情報から、「好きな果物」に応じてメッセージを表示してください。
//
// 【条件】
// ✅ 配列 `$users` に「名前」と「好きな果物」がセットで格納されています
// ✅ 好きな果物が：
//     - 「みかん」なら → 「○○さんは人気No.1のみかん好きです！」
//     - 「りんご」なら → 「○○さんは定番のりんご派です！」
//     - 「バナナ」なら → 「○○さんは栄養満点のバナナが好きです！」
//     - 上記以外なら → 「○○さんは○○が好きです！」と表示
// ✅ それぞれのメッセージを1行ずつ `<br>` を使って表示してください
//
// 【ヒント】
// 💡 配列 `$users` を foreach 文でループし、`$user["fruit"]` に応じて `if / elseif / else` で判定します

// ✅ ここから下に『自分で考えた日本語の処理手順』をコメントで書いてください。
// （ここに自分で日本語の処理文を書いてください）

// 配列$usersに、「名前」と「好きな果物」をセットで定義する。
// 変数$fruitは$users["fruit"]とする。
// 変数$nameは$users["name"]とする。
//
// 好きな果物が：
//     - 「みかん」なら → 「○○さんは人気No.1のみかんが好きです！」
//     - 「りんご」なら → 「○○さんは定番のりんご派です！」
//     - 「バナナ」なら → 「○○さんは栄養満点のバナナが好きです！」
//     - 上記以外なら → 「○○さんは○○が好きです！」と表示
//
// 🔍 ポイント解説
//      処理	                    内容
// $user['name']	            名前を取り出す（文字列）
// $user['fruit']	             好きな果物を取り出す（文字列）
// foreach ($users as $user)	各ユーザー情報を1件ずつ処理
// if ($fruit === "みかん")	    条件に応じて分岐処理//
//


// ✅ その下に『自分でPHPコード』を書いてください。
// （ここに自分でPHPコードを書いてください）
$users = [
    ["name" => "さくら", "fruit" => "りんご"],
    ["name" => "たけし", "fruit" => "バナナ"],
    ["name" => "ゆうこ", "fruit" => "りんご"],
    ["name" => "けん", "fruit" => "みかん"],
    ["name" => "あい", "fruit" => "バナナ"],
    ["name" => "まさし", "fruit" => "いちじく"]
];

foreach ($users as $user) {
    $name = $user["name"];
    $fruit = $user["fruit"];

    if ($fruit === "みかん") {
        echo "{$name}さんは人気No.1のみかんが好きです！<br>";
    } elseif ($fruit === "りんご") {
        echo "{$name}さんは定番のりんご派です！<br>";
    } elseif ($fruit === "バナナ") {
        echo "{$name}さんは栄養満点のバナナが好きです！<br>";
    } else {
        echo "{$name}さんは{$fruit}が好きです！<br>";
    }
}
