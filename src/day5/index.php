<?php
// 🎯 Day5：ループ処理の応用 - 入れ子ループ（ネスト）
//
// 🔰 解説：
// ループの中にさらにループを入れることを「入れ子（ネスト）ループ」と言います。
// 例えば、縦横に並ぶ表や、複数の組み合わせを出力したいときに使います。

echo "【ネストされた for 文の例】<br><br>";

// 🔽 ここでは、1〜3段目までの「九九の一部」のような出力をします
// 外側のループ：段（1〜3段）
// 内側のループ：掛ける数（1〜3）

for ($i = 1; $i <= 3; $i++) {
    // 各段の見出し
    echo "【{$i}の段】<br>";

    for ($j = 1; $j <= 3; $j++) {
        // 計算と出力（横方向）
        $result = $i * $j;
        echo "{$i} × {$j} = {$result}<br>";
    }

    // 段ごとの区切り
    echo "<br>";
}


// | 項目           |      説明                                |
// | ------------- | ---------------------------------------  |
// | `for` の入れ子 | 外のループが1回動くごとに、内側が全部回る   |

// | `$i`, `$j` の変化 | \$iが1のとき、\$jは1〜3をループ。次に\$i=2、また\$jは1〜3… |

// | 実用例    | 九九、表形式データ、組み合わせなどに応用可能     |

