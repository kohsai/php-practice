<?php
// 🎯 Day5：ネストループの応用（条件付きの組み合わせ）
//
// 📝 解説：
// 1〜5の数字の中から「異なる2つの数字の組み合わせ」を出力します。
// ただし、同じ数字のペア（例: 2と2）は除外します。

echo "【1〜5の異なる組み合わせを表示】<br><br>";

for ($a = 1; $a <= 5; $a++) {
    for ($b = 1; $b <= 5; $b++) {
        if ($a !== $b) {
            echo "{$a} と {$b}<br>";
        }
    }
}

echo "<br>";

// ✨ 解説ポイント
// 項目	            説明

// $a !== $b	同じ数字同士の組み合わせを除外（例: 1と1 2と2　など）

// ネストループの応用	for文の中で if 条件を加えるパターン

// 現実的な応用例	トランプ、対戦表、組み合わせ問題 など

// ーーーーーーーーーーーーーーーーーーーーーー

// ✅ アレンジ：段の切り替わりごとに空行（改行）を追加

echo "【段の間に改行を設けて、1〜5の異なる組み合わせを表示】<br><br>";

for ($a = 1; $a <= 5; $a++) {
    for ($b = 1; $b <= 5; $b++) {
        if ($a !== $b) {
            echo "{$a} と {$b}<br>";
        }
    }
    // 段（$aのループ）が終わるごとに1行改行
    echo "<br>";
}



