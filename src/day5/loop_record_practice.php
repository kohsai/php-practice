// 変数 `$users` に、複数人の「名前」と「好きな果物」の情報を持つ連想配列を用意
// foreach 文で1人ずつ取り出し、「好きな果物」が「みかん」の人の名前だけを記録（配列に追加）
// $nameを活用するので$user = ["name"]を定義しておく。
// ループ後に、記録した名前を1人ずつ表示（「○○さんはみかんが好きです」）
// 最後に「みかん好きの人数：○人」と表示する
// 表示は `<br>` を使ってブラウザに見やすく出力すること


// ✅ その下に『自分でPHPコード』を書いてください。
// （ここに自分でPHPコードを書いてください）

$users = [
    ["name" => "KOH", "fruit" => "バナナ"],
    ["name" => "Yuki", "fruit" => "りんご"],
    ["name" => "Taro", "fruit" => "バナナ"],
    ["name" => "Hana", "fruit" => "みかん"],
    ["name" => "Ken",  "fruit" => "みかん"],
];

$mikan_lovers = [];

foreach ($users as $user) {
    if ($user["fruit"] === "みかん") {
        $name = $user["name"];
        $mikan_lovers[] = $name;
    }
}

echo "みかんが好きな人の一覧:<br>";
foreach ($mikan_lovers as $name) {
    echo ("{$name}さん<br>");
}

// 合計人数を表示
echo "<br>みかん好きの人数： " . count($mikan_lovers) . " 人です<br>";
