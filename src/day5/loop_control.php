<?php
// 🎯 Day5 ループ制御（loop_control.php）
// 📘 テーマ：break / continueの使い方

// 🔹 break：ループを途中で終了する
// 🔹 continue：その周のループをスキップし、次に進む

echo "【breakの例】<br><br>";

// 1〜10まで表示。ただし、5になったらループを終了
for ($i = 1; $i <= 10; $i++) {
    if ($i === 5) {
        echo "5になったのでループを終了します<br>";
        break; // 🔚 ループを終了
    }
    echo "カウント: $i<br>";
}

echo "<br><br>【continueの例】<br><br>";

// 1〜10まで表示。ただし、偶数はスキップ
for ($i = 1; $i <= 10; $i++) {
    if ($i % 2 === 0) {
        continue; // 🔄 偶数はスキップ
    }
    echo "奇数: $i<br>";
}

// ✅ ポイント解説
// 構文	            説明
// break;	条件に一致したら、ループ自体を終了する
// 例：ユーザーがキャンセルしたとき、検索結果が見つかったときなど

// continue;	条件に一致したら、その1回分をスキップする
// 例：不正な入力は無視して次へ、など

