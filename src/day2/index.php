<?php
// PHPのタイムゾーンを日本時間に設定（UTC→Asia/Tokyo）
date_default_timezone_set("Asia/Tokyo");


echo "<h1>Day2 - PHP定数と時刻処理の復習</h1>";

/*
=========================================
✅ 練習問題1：const を使った固定値の出力
-----------------------------------------
🎯 目的：
「静的な定数は関数の外でしか使えない」ことを確認する

📝 問題：
- 定数名：APP_NAME
- 値："KOH's App"
- 関数の外で定義
- 出力結果：このアプリ名は KOH's App です。
=========================================
*/
const APP_NAME = "KOH's App";
echo "<p>このアプリ名は " . APP_NAME . " です。</p>";


/*
=========================================
✅ 練習問題2：define() を使って関数の中で定義
-----------------------------------------
🎯 目的：
「動的な定数は関数の中でも使える」ことを確認する

📝 問題：
- 関数名：setCreator
- 定数名：CREATOR
- 値："KOH"
- 出力結果：このアプリの作成者は KOH です。
=========================================
*/
function setCreator()
{
    define("CREATOR", "KOH");
    echo "<p>このアプリの作成者は " . CREATOR . " です。</p>";
}
setCreator();


/*
=========================================
✅ 練習問題3（応用）：条件で定数を変える
-----------------------------------------
🎯 目的：
実行時に条件に応じて define() で動的に定義される様子を理解する

📝 問題：
時間帯によって以下の挨拶を出力
- 午前（0〜11時）："おはようございます"
- 午後（12〜17時）："こんにちは"
- 夜（18時以降）："こんばんは"

出力例：
GREETING: おはようございます
=========================================
*/
$hour = date("H"); // 現在の時間（時のみ）を取得

if ($hour < 12) {
    define("GREETING", "おはようございます");
} elseif ($hour < 18) {
    define("GREETING", "こんにちは");
} else {
    define("GREETING", "こんばんは");
}

echo "<p>GREETING: " . GREETING . "</p>";
echo "<p>現在の時刻：" . date("H:i:s") . "</p>";
?>