<?php
// 🎓 Day6：関数（function）の基本（index.php）

// ✅ 【1】関数とは？
// 「よく使う処理」をまとめておき、必要なときに呼び出せる仕組みです。
// 例：あいさつを表示する処理を1つの関数として定義しておけば、何度でも使えます。

// ✅ 【2】関数の基本構文
// function 関数名() {
//     実行したい処理;
// }

function greet()
{
    echo "こんにちは！<br>";
}

// ✅ 【3】関数の呼び出し
// 定義した関数は、「関数名()」で呼び出します。

greet(); // 「こんにちは！」と表示される
greet(); // もう一度表示してみる（関数の再利用）

// ✅ 【4】引数（ひきすう）を使って、呼び出す内容を変える
// 関数に「値」を渡して、処理の中で使うことができます。

function greet_person($name)
{
    echo "こんにちは、{$name}さん！<br>";
}

greet_person("KOH");     // 「こんにちは、KOHさん！」と表示
greet_person("田中");    // 「こんにちは、田中さん！」と表示

// ✅ 【5】戻り値（return）を使って、結果を受け取る
// 計算などの処理をして、結果だけを呼び出し元に返すことができます。

function add($a, $b)
{
    $sum = $a + $b;
    return $sum; // 計算結果を返す
}

// 戻り値を受け取って表示
$result = add(3, 5);
echo "3 + 5 の結果は: {$result}<br>";

$result2 = add(10, 20);
echo "10 + 20 の結果は: {$result2}<br>";

// ✅ 【まとめ】
// ・function ～ で関数を定義
// ・関数名() で呼び出し
// ・引数を渡せる
// ・return で結果を返せる
