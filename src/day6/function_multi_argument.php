<?php
// 📘 Day6 教材（function_multi_argument.php）

// 🏷 テーマ：複数の引数を受け取る関数

// 🔸 これまでの復習：
// 関数には「1つの引数」を渡して処理を行うことができました。
// たとえば、名前を受け取って「○○さん、ようこそ！」と表示するなど。

// 🔸 今回の目的：
// 複数の引数を受け取って、より柔軟な処理をする関数を学びます。

// ✅ 【1】2つの引数を受け取る関数の定義
// 引数はカンマで区切って複数指定できます。

function showMessage($name, $age)
{
    echo "{$name}さんは {$age} 歳です。<br>";
}

// ✅ 【2】関数を呼び出すときも、2つの値を順番に渡す
showMessage("KOH", 28);
showMessage("Yuki", 32);
showMessage("Sota", 45);

// 🔍 解説：
// - 関数の引数は `$name` と `$age` の2つ
// - 呼び出すときに2つの値を渡すことで、関数内で両方の値を使った処理が可能になります
// - 呼び出すたびに異なる値を渡せば、毎回異なるメッセージが出力されます

// 🔰 ポイントまとめ：
// ・関数に引数を「複数」渡したい場合はカンマで区切って指定する
// ・渡す順番と数は、定義と呼び出しで一致させる必要がある
