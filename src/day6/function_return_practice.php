// 📝 【問題】
// 2つの数値を受け取って、合計を return する関数を作成してください。
// その関数を使って、任意の2つの数値を足し算し、結果を画面に表示してください。

// 【条件】
// ✅ 関数名は `calculateSum` とする
// ✅ 引数に2つの数値（整数）を受け取る
// ✅ その合計を return で返す
// ✅ 関数を呼び出したあと、戻り値を変数に代入し、画面に表示する
// ✅ 表示形式は「合計は○○です。」とし、1行で `<br>` を使うこと

// 【ヒント】
// 💡 計算結果を return した後、echo 文で表示する必要があります。
// 💡 表示処理は関数の外で行います。


// ✅ ここから下に『自分で考えた日本語の処理手順』をコメントで書いてください。
// （ここに自分で日本語の処理文を書いてください）
//  関数 calculateSum を定義。
//  引数に二つの数値を受けとる
//  合計をreturnで返す
//  関数を呼び出したあと、戻り値を変数に代入し、画面に表示する


// ✅ その下に『自分でPHPコード』を書いてください。
// （ここに自分でPHPコードを書いてください）

function calculateSum($a, $b)
{
    $sum = $a +$b;
    return $sum;
}

$result = calculateSum(12, 18);
echo "12 + 18 の合計は：{$result}<br>";