<?php
// 🎓 Day6 教材（function_argument_judge.php）

// 🧠 【学習テーマ】関数の引数：値を受け取る仕組み

// ✅ 関数には「引数」を設定することで、外から値を受け取ることができます。
// ✅ 受け取った値をもとに処理を実行し、結果を表示するなどの処理が可能です。

// ✅ 例：1つの引数を受け取って挨拶する関数を定義します
function greet($name)
{
    // 引数$nameに外から受け取った値が入ります
    echo "{$name}さん、こんにちは！<br>";
}

// ✅ 関数の呼び出し：引数に「KOH」などの名前を渡します
greet("KOH");       // 出力：KOHさん、こんにちは！
greet("佐藤");      // 出力：佐藤さん、こんにちは！
greet("Yuki");      // 出力：Yukiさん、こんにちは！

// ✅ 引数の変数名と、呼び出すときの変数名は一致していなくても問題ありません
$student = "田中";
greet($student);    // 出力：田中さん、こんにちは！

// 💬 ポイントまとめ
// - 関数の定義では、カッコ内に引数（$nameなど）を書く
// - 呼び出すときに渡す値（文字列や変数）は、定義時の変数名と違っていてもOK
// - 関数は「使い回し」ができ、複数回呼び出して異なる結果を得られます
