// 📝 【問題】
// 「2つの数字を足し算する関数」を作成し、2回ほど呼び出して結果を表示してください。

// 【条件】
// ✅ 関数名は `addNumbers` とする
// ✅ 2つの引数を受け取り、その合計を return で返す
// ✅ 関数を2回以上呼び出して、それぞれの結果を表示する
// ✅ 表示は `<br>` を使ってブラウザで見やすくする

// 【ヒント】
// 💡 return を使うと、関数の外に値を返せます。
// 💡 関数の呼び出し → `$result = addNumbers(3, 5);`

// -------------------------------------------------------------

// ✅ ここから下に『自分で考えた日本語の処理手順』をコメントで書いてください。
// （ここに自分で日本語の処理文を書いてください）
//  関数addNumbersを定義する。
//  $a と $b の合計を$sumとし、returnで返す。
//  数字を変えて２回計算し、それぞれの結果を表示する。
//
// ✅ その下に『自分でPHPコード』を書いてください。
// （ここに自分でPHPのコードを書いてください）
function addNumbers($a, $b)
{
    $sum = $a + $b;
    return $sum;
}

$result = addNumbers(18, 12);
echo "18 + 12 の結果は: {$result}<br>";

$result2 = addNumbers(15, 25);
echo "15 + 25 の結果は: {$result2}<br>";
