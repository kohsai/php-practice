<?php

// 🧠 練習問題（nested_if.php に記述）
// 以下の条件に沿ってif文を記述してください。

// ✅ 練習1：入店チェック
// 以下の条件に従い、「入店OK」「年齢制限」「マスク必須」のいずれかを表示してください。

// 20歳以上、マスク着用 → 「入店OK」

// 20歳以上、マスク未着用 → 「マスクを着用してください」

// 20歳未満 → 「未成年の方は入店できません」

// 変数例：
$age = 20;
$wearingMask = true;

if ($age >= 20) {
    if ($wearingMask) {
        echo "<p>入店OK</p>";
    } else {
        echo "<p>マスクを着用してください</p>";
    }
} else {
    echo "<p>未成年の方は入店できません</p>";
}


// ✅ 練習2：ログイン判定
// 以下の条件でユーザーの状態を表示してください。

// ログイン中かつ管理者 → 「管理者ページへようこそ」

// ログイン中かつ一般ユーザー → 「ようこそ、ユーザーさん」

// ログインしていない → 「ログインしてください」

// 変数例：
$isLoggedIn = true;
$isAdmin = true;

if ($isLoggedIn && $isAdmin) {
    echo "<p>管理者ページへようこそ</p>";
} elseif ($isLoggedIn && !$isAdmin) {
    echo "<p>ようこそ、ユーザーさん</p>";
} else {
    echo "<p>ログインしてください</p>";
}

