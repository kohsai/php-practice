<?php

// 練習⓵：天気に応じたメッセージ表示（if文） //

// 変数 $weather に天気（"晴れ", "雨", "曇り"など）を入れて、条件によってコメントを出し分けましょう。

// 期待される動作：
// "晴れ" → 「いい天気ですね！」
// "雨" → 「傘を忘れずに！」
// "曇り" → 「ちょっと暗いですね」
// その他 → 「天気がわかりません」 -->


$weather = "雨";

// if文で条件分岐を行う
if ($weather == "晴れ" ) {
    // 晴れの場合
    echo "<p>いい天気ですね！</p>";
} elseif ($weather == "雨") {
    // 雨の場合
    echo "<p>傘を忘れずに！</p>";
} elseif ($weather == "曇り") {
    // 曇りの場合
    echo "<p>ちょっと暗いですね</p>";
} else {
    // その他
    echo "<p>天気が分かりません。</p>";
}


