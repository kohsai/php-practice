$score = 92;

if ($score >= 90) {
    echo "<p>評価：A</p>";
} elseif ($score >= 70) {
    echo "<p>評価：B</p>";
} elseif ($score >= 50) {
    echo "<p>評価：c</p>";
} else {
    echo "<p>評価：d</p>";
}


